package level3;

public class level3_5 {
    public static void PrintOneHang(int num){
        for(int i=1;i<=num;i++){
            System.out.print(i + "\t");
        }
        System.out.print("\n");
    }
    public static void main(String[] args){
        for(int j=1;j<=4;j++){
            PrintOneHang(j);
        }
        System.out.println("23物联网1时锐202020211111");
    }
}
